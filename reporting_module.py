class VulnReport(FPDF):

    def header(self):
        self.set_fill_color(30, 30, 30)
        self.rect(0, 0, 210, 18, "F")
        self.set_font("Helvetica", "B", 13)
        self.set_text_color(255, 255, 255)
        self.cell(0, 18, "Vuln_VAS  |  Security Assessment Report", align="C")
        self.ln(5)

    def footer(self):
        self.set_y(-12)
        self.set_font("Helvetica", "I", 8)
        self.set_text_color(150, 150, 150)
        self.cell(0, 10, f"Generated by Vuln_VAS  |  Page {self.page_no()}", align="C")

    def section_title(self, title: str):
        self.set_font("Helvetica", "B", 11)
        self.set_fill_color(45, 45, 45)
        self.set_text_color(255, 255, 255)
        self.cell(0, 8, f"  {title}", ln=True, fill=True)
        self.set_text_color(30, 30, 30)
        self.ln(2)

    def kv(self, key: str, value: str):
        self.set_font("Helvetica", "B", 9)
        self.set_text_color(80, 80, 80)
        self.cell(42, 6, key + ":", ln=False)
        self.set_font("Helvetica", "", 9)
        self.set_text_color(30, 30, 30)
        self.multi_cell(0, 6, str(value))

    def severity_badge(self, sev: str):
        colors = {
            "Critical": (231, 76, 60),
            "High"    : (230, 126, 34),
            "Medium"  : (241, 196, 15),
            "Low"     : (46, 204, 113),
            "Unknown" : (149, 165, 166),
        }
        r, g, b = colors.get(sev, (149, 165, 166))
        self.set_fill_color(r, g, b)
        self.set_text_color(255, 255, 255)
        self.set_font("Helvetica", "B", 8)
        self.cell(22, 5, sev.upper(), align="C", fill=True, ln=False)
        self.set_text_color(30, 30, 30)


def generate_pdf(report: dict, output_path: str) -> str:
    pdf = VulnReport()
    pdf.set_auto_page_break(auto=True, margin=15)
    pdf.add_page()

    target    = report.get("target", "N/A")
    scan_time = report.get("scan_time", "N/A")
    vulns     = report.get("vulnerabilities", [])
    risk      = report.get("risk_summary", {})

    # ── Cover info ───────────────────────────
    pdf.set_font("Helvetica", "B", 18)
    pdf.set_text_color(30, 30, 30)
    pdf.ln(4)
    pdf.cell(0, 10, "Vulnerability Assessment Report", ln=True, align="C")
    pdf.set_font("Helvetica", "", 10)
    pdf.set_text_color(100, 100, 100)
    pdf.cell(0, 6, f"Target: {target}   |   Scanned: {scan_time}", ln=True, align="C")
    pdf.ln(6)

    # ── Risk Summary ─────────────────────────
    pdf.section_title("Risk Summary")
    pdf.set_font("Helvetica", "", 9)
    col_w = 38
    for sev in ["critical", "high", "medium", "low"]:
        count = risk.get(sev, 0)
        label = sev.capitalize()
        r, g, b = {
            "critical": (231, 76, 60),
            "high"    : (230, 126, 34),
            "medium"  : (241, 196, 15),
            "low"     : (46, 204, 113),
        }[sev]
        pdf.set_fill_color(r, g, b)
        pdf.set_text_color(255, 255, 255)
        pdf.set_font("Helvetica", "B", 9)
        pdf.cell(col_w, 8, f"  {label}: {count}", fill=True, ln=False, border=0)
    pdf.ln(12)
    pdf.set_text_color(30, 30, 30)
    pdf.set_font("Helvetica", "B", 10)
    pdf.cell(0, 6, f"Overall Risk Score: {risk.get('overall_risk_score', 'N/A')} / 100", ln=True)
    pdf.ln(4)

    # ── Vulnerabilities ──────────────────────
    pdf.section_title(f"Vulnerabilities Detected ({len(vulns)})")
    pdf.ln(2)

    for i, v in enumerate(vulns, 1):
        if pdf.get_y() > 250:
            pdf.add_page()

        pdf.set_font("Helvetica", "B", 10)
        pdf.set_text_color(30, 30, 30)
        pdf.cell(10, 6, f"{i}.", ln=False)
        pdf.cell(0, 6, f"{v['service']} {v['version']}  (Port {v['port']}/{v.get('proto','tcp')})", ln=False)
        pdf.ln(6)

        pdf.set_x(12)
        pdf.severity_badge(v.get("severity", "Unknown"))
        pdf.set_font("Helvetica", "", 9)
        pdf.cell(0, 5, f"   CVE: {v.get('cve','N/A')}   CVSS: {v.get('cvss_score', 0):.1f}   "
                       f"Exploit: {v.get('exploit_reference','None')}   Confidence: {v.get('confidence','N/A')}", ln=True)

        pdf.set_x(12)
        pdf.set_font("Helvetica", "I", 8)
        pdf.set_text_color(80, 80, 80)
        pdf.multi_cell(0, 5, v.get("description", "")[:250])

        pdf.set_text_color(30, 30, 30)
        # Remediation hint
        pdf.set_x(12)
        pdf.set_font("Helvetica", "B", 8)
        pdf.set_text_color(0, 102, 204)
        pdf.cell(0, 5, "Recommendation: " + get_recommendation(v), ln=True)
        pdf.set_text_color(30, 30, 30)
        pdf.ln(3)

    # ── Disclaimer ───────────────────────────
    pdf.add_page()
    pdf.section_title("Disclaimer")
    pdf.set_font("Helvetica", "", 9)
    pdf.multi_cell(0, 5,
        "This report was generated by Vuln_VAS for authorized security assessment only. "
        "All testing was performed with explicit consent on whitelisted targets. "
        "Unauthorized use of this tool or acting on its findings without proper authorization "
        "may violate applicable laws. The authors accept no liability for misuse."
    )

    pdf.output(output_path)
    return output_path


