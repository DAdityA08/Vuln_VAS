    scan_time = report.get("scan_time", "N/A")
    vulns     = report.get("vulnerabilities", [])
    risk      = report.get("risk_summary", {})

    st.title(f"🛡️ Vuln_VAS  |  {target}")
    st.caption(f"Scan time: {scan_time}   |   Services analysed: {report.get('total_services', len(vulns))}")
    st.divider()

    # ── KPI Row ──────────────────────────────
    c1, c2, c3, c4, c5, c6 = st.columns(6)
