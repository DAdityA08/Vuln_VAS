    c3.metric("🟡 Medium",   risk.get("medium",   0))
    c4.metric("🟢 Low",      risk.get("low",      0))
    c5.metric("📊 Risk Score", f"{risk.get('overall_risk_score', 0)}/100")
    c6.metric("🎯 Total Vulns", len(vulns))

    st.divider()

    # ── Charts Row ───────────────────────────
