# HELPER
# ─────────────────────────────────────────────
def get_recommendation(vuln: dict) -> str:
    sev = vuln.get("severity", "").lower()
    svc = vuln.get("service", "").lower()

    generic = {
        "critical": "Patch or disable this service immediately.",
        "high"    : "Update to the latest stable version and apply vendor patches.",
        "medium"  : "Review configuration and apply available security updates.",
        "low"     : "Monitor for further exposure and plan remediation.",
    }
    specific = {
        "vsftpd"  : "Upgrade vsftpd; consider disabling anonymous FTP access.",
        "openssh" : "Upgrade OpenSSH; enforce key-based auth, disable root login.",
        "apache"  : "Upgrade Apache httpd; disable directory listing; review .htaccess.",
        "mysql"   : "Upgrade MySQL; restrict remote root login; use strong credentials.",
        "samba"   : "Upgrade Samba; disable SMBv1; restrict share permissions.",
    }
    for key, rec in specific.items():
        if key in svc:
            return rec
    return generic.get(sev, "Consult vendor advisory for remediation steps.")


