# STANDALONE EXPORT (no Streamlit)
# ─────────────────────────────────────────────
def export_reports(report: dict, output_dir: str = "."):
    """Generate PDF + HTML + JSON from a report dict without Streamlit."""
    Path(output_dir).mkdir(parents=True, exist_ok=True)
    target_clean = report.get("target", "target").replace(".", "_")

    pdf_path  = str(Path(output_dir) / f"vuln_report_{target_clean}.pdf")
    html_path = str(Path(output_dir) / f"vuln_report_{target_clean}.html")
    json_path = str(Path(output_dir) / f"vuln_analysis_{target_clean}.json")

    generate_pdf(report,  pdf_path)
    generate_html(report, html_path)
    with open(json_path, "w") as f:
        json.dump(report, f, indent=4)

    print(f"[✓] PDF  → {pdf_path}")
    print(f"[✓] HTML → {html_path}")
    print(f"[✓] JSON → {json_path}")
    return pdf_path, html_path, json_path


